% See the accompanying file LICENSE.txt for licensing details.


if exist('fname','var') && ~empty(fname)
    f=read_dlm_file(fname);
    c=zeros(3,length(f)-1);
    for i=2:length(f)
      c(1,i-1)=str2num(f{i}{2});
      c(2,i-1)=str2num(f{i}{3});
      c(3,i-1)=str2num(f{i}{4});
    end
    c=c/255;
    c=c';
else
    % default values from colorscheme.txt
    c =[255	0	0;...
    	0	255	0;...
    	255	255	0;...
    	0	190	255;...
     	113	48	255;...
		255	160	0;...
		0	128	0;...
		131	131	0;...
		0	255	255;...
		0	0	255;...
		220	220	220;...
		0	128	192;...
		255	0	255;...
		0	0	0;...
		255	255	255;...
		255	255	255;...
		255	255	255;...
		255	255	255;...
		176	39	39;...
		120	120	120;...
		130	56	115;...
		128	64	0;...
		255	80	0;...
		220	185	225]/255;
end
    
%{
f=read_dlm_file(fname);
n=size(D.sdesc,1);
c=zeros(3,n);
for i=2:length(f)
  s=find(cat(1,D.sup(:).TT)==(i-1));
  c(1,s)=str2num(f{i}{2});
  c(2,s)=str2num(f{i}{3});
  c(3,s)=str2num(f{i}{4});
end

D.supdesc=strvcat(D.supdesc,'Color R','Color G','Color B');
D.supacc=strvcat(D.supacc,'CR','CG','CB');
D.supdat=[D.supdat; c];
%}